    private void insertar(){
        String name = jtextfield(txtName);
        String table = jtextfield(txtTable);
        int id=m.getMax(db.query("select id from guests"));
        String [] insertar = new String [5];
        insertar[0]=id+"";
        insertar[1]=jtextfield(txtName);
        insertar[2]=jtextfield(txtTable);
        insertar[3]="Absent";
        insertar[4]="0";
        db.insert("guests", insertar);
        limpiar();
        JOptionPane.showMessageDialog(this,"Inserted");
        txtName.requestFocus();
    }
    public String jtextfield(JTextField e){
        if(!e.getText().equals(""))return e.getText();
        else return null;
    }
    
