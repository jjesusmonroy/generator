    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date date = new java.util.Date();
        String dateString = sdf.format(date);
        
        Process p=null;
        try{
            File f = fc.getSelectedFile();
            String path = f.getAbsolutePath();
            path = path.replace("\\", "/");
            path = path + "_"+ dateString + ".sql";
            Runtime runtime = Runtime.getRuntime();
            p=runtime.exec("C:/Program Files/MySQL/MySQL Server 5.7/bin/mysqldump.exe -uroot -proot --add-drop-database -B generator -r"+path);
            
            int processComplete = p.waitFor();
            if(processComplete==0){
                JOptionPane.showMessageDialog(this, "Succesfully generated, backup file was ");
            }
            else {
                JOptionPane.showMessageDialog(this, "Error, didn't generate our backup file");
            }
        }catch(HeadlessException | IOException | InterruptedException e){
            System.out.println("Error on generarFile at f GeneratorFrame\n"+e.getMessage());
        }
        
    }//GEN-LAST:event_btnGenerarActionPerformed

